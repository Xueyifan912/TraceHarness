# ── Basic tool handlers ──

def run_create_task(subject: str, description: str = "",
                    blockedBy: list[str] | None = None) -> str:
    task = create_task(subject, description, blockedBy)
    deps = f" (blockedBy: {', '.join(blockedBy)})" if blockedBy else ""
    print(f"  \033[34m[create] {task.subject}{deps}\033[0m")
    return f"Created {task.id}: {task.subject}{deps}"


def run_list_tasks() -> str:
    tasks = list_tasks()
    if not tasks:
        return "No tasks."
    return "\n".join(
        f"  {format_task_line(t)}"
        for t in tasks)


def run_get_task(task_id: str) -> str:
    try:
        return get_task_json(task_id)
    except (FileNotFoundError, ValueError) as exc:
        return f"Error: task {task_id} not found ({exc})"

def run_claim_task(task_id: str) -> str:
    try:
        return claim_task(task_id, owner="agent")
    except (FileNotFoundError, ValueError) as exc:
        return f"Error: task {task_id} not found ({exc})"

def run_complete_task(task_id: str) -> str:
    try:
        return complete_task(task_id, owner="agent")
    except (FileNotFoundError, ValueError) as exc:
        return f"Error: task {task_id} not found ({exc})"

def run_spawn_teammate(name: str, role: str, prompt: str) -> str:
    return spawn_teammate_thread(name, role, prompt)

def run_send_message(to: str, content: str) -> str:
    try:
        BUS.send("lead", to, content)
    except ValueError as exc:
        return f"Error: {exc}"
    return f"Sent to {to}"

def run_check_inbox() -> str:
    msgs = consume_lead_inbox(route_protocol=True)
    if not msgs:
        return "(inbox empty)"
    lines = []
    for m in msgs:
        meta = m.get("metadata", {})
        req_id = meta.get("request_id", "")
        tag = f" [{m['type']} req:{req_id}]" if req_id else f" [{m['type']}]"
        lines.append(f"  [{m['from']}]{tag} {m['content'][:200]}")
    return "\n".join(lines)

def run_team_status() -> str:
    return team_status()

def run_connect_mcp(name: str) -> str:
    return connect_mcp(name)


def run_memory_read() -> str:
    return read_memory_for_tool()


def run_memory_append(content: str) -> str:
    return append_memory(content)


def assemble_tool_pool() -> tuple[list[dict], dict]:
    tools = list(BUILTIN_TOOLS)
    handlers = dict(BUILTIN_HANDLERS)
    mcp_tools, mcp_handlers = mcp_tool_entries()
    tools.extend(mcp_tools)
    handlers.update(mcp_handlers)
    return tools, handlers


